#pragma once

namespace KatanaEngine
{
	namespace GUI
	{

		class Panel : public ComponentContainer
		{

		public:

			Panel() { }
			virtual ~Panel() { }

			virtual void Draw(SpriteBatch *pSpriteBatch);

			virtual void HandleInput(const InputState *pInput)
			{
				ComponentContainer::HandleInput(pInput);

				if (IsMouseOver() && pInput->IsNewMouseButtonPress(MouseButton::LEFT))
				{
					Point s = GetSize();
					s.X += 10;
					SetSize(s);
				}
			}

		};
	}
}

